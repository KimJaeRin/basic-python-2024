#  사칙연산 : + - * /(나누기), //(정수로 나누기), %(나눈 나머지 값)
# **(제승)
# 비교연산 : =(값 할당), ==(비교), >, <, >=, <= , !=(같지 않다)
# 논리연산 : and, or, not

print( 2 * 10)
print( 2 ** 10)

print( 2 * 10)

print( 5 / 2) # 실수로 나누기
print( 5 // 2) # 정수로 나누기
print( 5 % 2)  # 나눈 나머지

print( 5 == 4)
print( 5 > 4)
print( 5 >= 4)
print( 5 <= 4)
print( 5 < 4)
print( 5 != 4)

















